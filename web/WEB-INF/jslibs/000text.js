function textjaccardsimilarity(str1,str2) {
    return text.textJaccardSimilarity(str1,str2);
}

function textlongestcommonsubsequence(str1,str2) {
    return text.textLongestCommonSubsequence(str1,str2);
}

function textlongestcommonsubsequenceDistance(str1,str2) {
    return text.textLongestCommonSubsequenceDistance(str1,str2);
}

function texthammingdistance(str1,str2) {
    return text.textHammingDistance(str1,str2);
}

function textfuzzyscore(str1,str2,locale) {
    return text.textFuzzyScore(str1,str2,locale);
}

